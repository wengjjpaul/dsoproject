package iss.pos.promotion;

import java.util.List;

public class Promotion {

	
	
	
	protected boolean sameSku = false;
	protected boolean sameStyle = false;
	

	
	public Promotion() {
		
	}

	public boolean isSameSku() {
		return sameSku;
	}

	public void setSameSku(boolean sameSku) {
		this.sameSku = sameSku;
	}

	public boolean isSameStyle() {
		return sameStyle;
	}

	public void setSameStyle(boolean sameStyle) {
		this.sameStyle = sameStyle;
	}

	
	
	
}
